package com.oops;

public class Oops {
	
	
	public static int sum(int a, int b) {
		return a+b;
	}
	public static int sum(int a, int b, int c) {
		return a+b+c;
	}
	public static String sum(String name1, String name2) {
		return name1+name2;
	}
	
	
	public static void main(String[] args) {
		System.out.println(7+5);
		System.out.println(7+5+8);
		System.out.println("Guru"+"Anju");
	}

}
